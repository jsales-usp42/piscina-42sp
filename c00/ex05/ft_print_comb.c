#include <unistd.h>

void	ft_print_comb(void)
{
	char	dig[3];

	dig[0] = '/';
	while (++dig[0] <= '7')
	{
		dig[1] = dig[0] + 1;
		while (dig[1] <= '8')
		{
			dig[2] = dig[1] + 1;
			while (dig[2] <= '9')
			{
				write(1, &dig[0], 1);
				write(1, &dig[1], 1);
				write(1, &dig[2], 1);
				if (!(dig[0] == '7' && dig[1] == '8' && dig[2] == '9'))
					write(1, ", ", 2);
				dig[2]++;
			}
			dig[1]++;
		}
	}
}
